
export function drawCar(steeringAngle) {
    allWheels(steeringAngle)
    carWindshield()
    carFront()
    carRear()
    rearDetail()
    frontDetail()
    finalTouches()
}
    function allWheels(steeringAngle) {
    push()
    translate(35.5, 0, 46.9)
    rotateY(steeringAngle)
    wheel()
    pop()
    push()
    translate(35.5, 0, -46.9)
    wheel()
    pop()
    push()
    translate(-35.5, 0, 46.9)
    rotateY(180 + steeringAngle)
    wheel()
    pop()
    push()
    translate(-35.5, 0, -46.9)
    rotateY(180)
    wheel()
    pop()
}

function wheel() {
    push()
    rotateZ(90)
    fill(10, 10, 10)
    cylinder(10, 10)
    translate(0, -1, 0)
    rotateX(90)
    translate(0, 0, 4)
    fill(10, 10, 10)
    torus(9, 1)
    fill(100, 100, 100)
    torus(8, 0.5)
    rotateX(90)
    cylinder(2.5, 2)
    rotateX(90)
    function spokes() {
        cylinder(0.5, 14.5)
        rotateZ(72)
    }
    for (let x = 0; x < 5; x++) {
        spokes();
    }
    pop()
}

